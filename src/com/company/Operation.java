package com.company;

public class Operation {
    public static int Sum(final int number1, final int number2) {
        return number1 + number2;
    }

    public static int Sub(final int number1, final int number2) {
        return number1 - number2;
    }

    public static int Mult(final int number1, final int number2) {
        return number1 * number2;
    }

    public static int Div(final int number1, final int number2) {
        return number1 / number2;
    }
}
